#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*temp;

	temp = 0;
	while (*s)
	{
		if (*s == c)
			temp = (char *)s;
		++s;
	}
	if (*s == c)
		temp = (char *)s;
	return (temp);
}
