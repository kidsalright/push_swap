#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = -1;
	ft_bzero(dst, len);
	while (++i < len && src[i])
		dst[i] = src[i];
	return (dst);
}
